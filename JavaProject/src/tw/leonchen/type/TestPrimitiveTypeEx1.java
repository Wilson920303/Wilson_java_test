package tw.leonchen.type;

public class TestPrimitiveTypeEx1 {

	public static void main(String[] args) {
		
		int number = 6;
		
		
		System.out.println("number = " + number) ;
		
		int a = 3;
		int b = 7;
		
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		
		int c = 4, d = 5;
		System.out.println("c = " + c + " d = " + d );

	}

}
